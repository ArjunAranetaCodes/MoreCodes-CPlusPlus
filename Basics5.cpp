//Basic Operations
#include<iostream>
using namespace std;
int main(){
 cout << "1 + 1 = " << 1 + 1 << endl; //addition
 cout << "1 - 1 = " << 1 - 1 << endl; //subtraction
 cout << "1 * 1 = " << 1 * 1 << endl; //multiplication
 cout << "1 / 1 = " << 1 / 1 << endl; //division
 cout << "4 % 2 = " << 4 % 2
 << " - there is no remainder" << endl; //modulus
 cout << "5 % 2 = " << 5 % 2
 << " - there is 1 remainder" << endl; //modulus
 return 0;
}
